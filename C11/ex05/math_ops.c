#include "do_op.h"

int	ft_suma(int n1, int n2)
{
	return (n1 + n2);
}

int	ft_resta(int n1, int n2)
{
	return (n1 - n2);
}

int	ft_multiplicacion(int n1, int n2)
{
	return (n1 * n2);
}

int	ft_division(int n1, int n2)
{
	return (n1 / n2);
}

int	ft_modulo(int n1, int n2)
{
	return (n1 % n2);
}
